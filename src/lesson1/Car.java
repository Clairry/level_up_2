package lesson1;

public class Car {
    private String name;
    private String year;
    private String kilometre;
    private  String price;

    public String getName() {
        return name;
    }

    public String getYear() {
        return year;
    }

    public String getKilometre() {
        return kilometre;
    }

    public String getPrice() {
        return price;
    }
}
